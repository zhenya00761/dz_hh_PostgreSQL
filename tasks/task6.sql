/*
 Таблица vacancy, может быть в разы больше, но количество регионов не увеличится (только
 на небольшое количество), поэтому для скорости лучше всего использовать индекс,
 чтобы не проходить все строки, а только по данному в запросе region_id
 */

create index idx_vacancy_region on vacancy(region_id);


/*
