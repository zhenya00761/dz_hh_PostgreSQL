 поэтому в таблице откликов лучше искать по вакансиям. Также так будет меньше повторений,
 чем если будет индекс по резюме (одним резюме можно отвечать на несколько вакансий).
 Также количество откликов будет постоянно увеличиваться, поэтому индекс тут необходим.
 */
 create index idx_response_vacancy on response(vacancy_id);

